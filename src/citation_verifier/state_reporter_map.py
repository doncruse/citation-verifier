def get_states_for_reporter(reporter: str) -> list[str]:
    """Get possible state court IDs for a given reporter abbreviation.

    Args:
        reporter: Reporter abbreviation (e.g., "P.3d", "Kan.", "Cal. App. 5th")

    Returns:
        List of state court ID strings, empty if reporter not found
    """
    if not reporter:
        return []

    # Normalize reporter string - remove spaces, dots, lowercase
    reporter_normalized = reporter.replace(" ", "").replace(".", "").lower()

    # Try REPORTER_VARIATIONS lookup (regional reporters)
    canonical = REPORTER_VARIATIONS.get(reporter_normalized)
    if canonical:
        return REPORTER_TO_STATES.get(canonical, [])

    # Try direct match against regional REPORTER_TO_STATES keys
    for key in REPORTER_TO_STATES:
        key_normalized = key.replace(".", "").replace(" ", "").lower()
        if key_normalized == reporter_normalized:
            return REPORTER_TO_STATES[key]

    # Try state-specific reporters
    for key in STATE_SPECIFIC_REPORTERS:
        key_normalized = key.replace(".", "").replace(" ", "").lower()
        if key_normalized == reporter_normalized:
            return STATE_SPECIFIC_REPORTERS[key]

    return []


def is_regional_reporter(reporter: str) -> bool:
    """Check if a reporter is a regional reporter (not state-specific).

    Args:
        reporter: Reporter abbreviation

    Returns:
        True if this is a regional reporter
    """
    return len(get_states_for_reporter(reporter)) > 1
